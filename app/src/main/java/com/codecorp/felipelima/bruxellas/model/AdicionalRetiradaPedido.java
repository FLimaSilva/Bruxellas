package com.codecorp.felipelima.bruxellas.model;

public class AdicionalRetiradaPedido {

    private String adicional;
    private String retirada;
    private String quant_adicional;
    private String quant_retirada;

    public AdicionalRetiradaPedido() {

    }

    public String getAdicional() {
        return adicional;
    }

    public void setAdicional(String adicional) {
        this.adicional = adicional;
    }

    public String getRetirada() {
        return retirada;
    }

    public void setRetirada(String retirada) {
        this.retirada = retirada;
    }

    public String getQuant_adicional() {
        return quant_adicional;
    }

    public void setQuant_adicional(String quant_adicional) {
        this.quant_adicional = quant_adicional;
    }

    public String getQuant_retirada() {
        return quant_retirada;
    }

    public void setQuant_retirada(String quant_retirada) {
        this.quant_retirada = quant_retirada;
    }
}
